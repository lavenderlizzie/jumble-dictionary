// Helper function to get all words from a dictionary category
export function getWordsFromCategory(category) {
  return Object.keys(wordDictionary[category] || {});
}

// Helper function to get definition
export function getDefinition(word, category) {
  return wordDictionary[category]?.[word.toLowerCase()] || "Definition not available";
}

// Helper function to find words matching a pattern (for crossword helper)
export function findWordsMatchingPattern(pattern, category = 'standard') {
  if (!pattern || pattern.trim() === '') {
    return [];
  }

  const cleanPattern = pattern.toLowerCase();
  
  if (/[^a-z_]/.test(cleanPattern)) {
    return [];
  }

  const words = getWordsFromCategory(category);
  const matchedWords = [];

  for (const word of words) {
    let match = true;

    if (word.length !== cleanPattern.length) {
      match = false;
    } else {
      for (let i = 0; i < cleanPattern.length; i++) {
        const pChar = cleanPattern[i];
        const wChar = word[i];
        
        if (pChar !== '_' && pChar !== wChar) {
          match = false;
          break;
        }
      }
    }

    if (match) {
      matchedWords.push(word);
    }
  }
  
  return matchedWords;
}

// Helper function to unscramble letters (finds ONLY perfect anagrams)
// Uses frequency counting to ensure identical letter frequencies and length
export function unscrambleLetters(letters, category = 'standard') {
  if (!letters || letters.trim().length < 2) {
    return [];
  }
  
  // Strip spaces and non-alphabetical characters, then normalize to lowercase
  const cleanLetters = letters.toLowerCase().replace(/[^a-z]/g, '');
  
  if (cleanLetters.length < 2) {
    return [];
  }

  // Count frequencies of the input letters
  const inputCounts = {};
  for (const char of cleanLetters) {
    inputCounts[char] = (inputCounts[char] || 0) + 1;
  }

  const words = getWordsFromCategory(category);
  const matchedWords = [];

  for (const word of words) {
    // A perfect anagram MUST have the exact same length
    if (word.length !== cleanLetters.length) {
      continue;
    }

    const wordCounts = {};
    let isMatch = true;

    for (const char of word.toLowerCase()) {
      wordCounts[char] = (wordCounts[char] || 0) + 1;
      // If the word requires a letter we don't have, or more of a letter than we have
      if (!inputCounts[char] || wordCounts[char] > inputCounts[char]) {
        isMatch = false;
        break;
      }
    }

    if (isMatch) {
      matchedWords.push(word.toLowerCase());
    }
  }
  
  return matchedWords;
}

// Helper function to find anagrams (all possible words from letters, including sub-anagrams)
export function findAnagrams(letters, category = 'standard') {
  if (!letters || letters.trim().length < 2) {
    return [];
  }
  
  const cleanLetters = letters.toLowerCase().replace(/[^a-z]/g, '');
  if (cleanLetters.length < 2) {
    return [];
  }

  // Count frequencies of the input letters
  const letterCounts = {};
  for (const char of cleanLetters) {
    letterCounts[char] = (letterCounts[char] || 0) + 1;
  }

  const words = getWordsFromCategory(category);
  const matchedWords = [];

  for (const word of words) {
    // A sub-anagram cannot be longer than the input letters
    if (word.length > cleanLetters.length) {
      continue;
    }

    const wordCounts = {};
    let isValid = true;

    for (const char of word.toLowerCase()) {
      wordCounts[char] = (wordCounts[char] || 0) + 1;
      // If the word requires a letter we don't have, or more of a letter than we have
      if (!letterCounts[char] || wordCounts[char] > letterCounts[char]) {
        isValid = false;
        break;
      }
    }

    if (isValid) {
      matchedWords.push(word.toLowerCase());
    }
  }
  
  return matchedWords;
}